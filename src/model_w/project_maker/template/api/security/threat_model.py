"""
Threat model definition for Model W Django + SvelteKit platform.
This is used to model the system architecture and identify potential security threats
using the pytm library and produce the required report and media.
"""

from pytm import (
    TM,
    Actor,
    Boundary,
    Classification,
    Data,
    Dataflow,
    Datastore,
    DatastoreType,
    ExternalEntity,
    Lifetime,
    Process,
    TLSVersion,
)

tm = TM("Model W Django + SvelteKit Platform - Comprehensive Threat Model")
tm.description = """
Comprehensive threat model for Model W project generator and generated projects.

This threat model covers:
1. The Model W Project Maker tool itself
2. Projects generated by the tool (Django + SvelteKit)
3. Optional components: Wagtail CMS, WebSockets (Channels), background workers
4. Infrastructure: PostgreSQL, Redis, object storage
5. CI/CD and deployment pipeline
6. Development and administrative access

Architecture:
- Public users -> Browser -> SvelteKit Frontend -> Django API
- CMS editors -> Browser -> Wagtail Admin (Django)
- Background workers (Procrastinate) -> PostgreSQL/Redis/Storage
- Observability: Sentry
- Deployment: Kerfu Foo, GitHub Actions
- Storage: PostgreSQL, Redis, DigitalOcean Spaces
"""

tm.isOrdered = True
tm.mergeResponses = True

tm.assumptions = [
    "All external communication uses HTTPS with TLS 1.2+",
    "Session authentication implemented using Django session cookies with secure flags",
    "CSRF protection enabled via Django middleware with proper token validation",
    "Redis and PostgreSQL accessible only within private network/VPC",
    "DigitalOcean App Platform provides network isolation and security groups",
    "Public object storage (DigitalOcean Spaces) used only for non-sensitive media assets",
    "Kerfu Foo manages deployment and secrets with secure access controls",
    "GitHub repository uses branch protection and requires code review",
    "Django settings use appropriate security configurations (DEBUG=False in production)",
    "SvelteKit implements proper client-side security controls",
    "All dependencies are regularly updated and monitored for vulnerabilities",
    "Database backups are encrypted and stored securely",
    "Access logs are collected and monitored for suspicious activity",
    "Multi-factor authentication enabled for administrative access",
]


# Boundaries
internet = Boundary("Public Internet")
internet.levels = [1, 2, 3]
internet.inScope = False
internet.maxClassification = Classification.PUBLIC

edge = Boundary("Edge Network / CDN / Load Balancer")
edge.levels = [1, 2]
edge.minTLSVersion = TLSVersion.TLSv12
edge.maxClassification = Classification.RESTRICTED # Internal but still non-sensitive

application = Boundary("Application Runtime (DigitalOcean App Platform)")
application.levels = [1, 2, 3]
application.minTLSVersion = TLSVersion.TLSv12 # Incoming from Edge is TLS
application.maxClassification = Classification.SENSITIVE
application.description = "Managed container environment for Django and SvelteKit"

internal_services = Boundary("Internal Data Services (VPC)")
internal_services.levels = [2, 3]
internal_services.minTLSVersion = TLSVersion.NONE 
internal_services.maxClassification = Classification.SENSITIVE

external_services = Boundary("External SaaS Services")
external_services.levels = [1, 2, 3]
external_services.maxClassification = Classification.SENSITIVE
external_services.minTLSVersion = TLSVersion.TLSv12

administration = Boundary("Administrative Systems")
administration.levels = [4]
administration.maxClassification = Classification.SECRET # Higher than app data
administration.minTLSVersion = TLSVersion.TLSv12

supply_chain = Boundary("Software Supply Chain")
supply_chain.levels = [4]
supply_chain.maxClassification = Classification.SECRET 
supply_chain.minTLSVersion = TLSVersion.TLSv12

dev_environment = Boundary("Development Environment")
dev_environment.levels = [5]
dev_environment.maxClassification = Classification.RESTRICTED

# Actors
User = Actor("Public User")
User.inBoundary = internet
User.levels = [1]

AuthenticatedUser = Actor("Authenticated User")
AuthenticatedUser.inBoundary = internet
AuthenticatedUser.levels = [2]

Editor = Actor("CMS Editor")
Editor.inBoundary = internet
Editor.levels = [3]

Developer = Actor("Developer")
Developer.inBoundary = dev_environment
Developer.levels = [4]

PlatformAdmin = Actor("Platform Administrator")
PlatformAdmin.inBoundary = administration
PlatformAdmin.levels = [5]

Attacker = Actor("Malicious Actor")
Attacker.inBoundary = internet
Attacker.levels = [0]  # External threat


# Processes
Browser = Process("User Browser")
Browser.inBoundary = internet
Browser.levels = [1]
Browser.OS = "Various"
Browser.allowsClientSideScripting = True  # Necessary for SvelteKit/JS execution
Browser.codeType = "Unmanaged"

Frontend = Process("SvelteKit Frontend")
Frontend.inBoundary = application
Frontend.levels = [2]
Frontend.OS = "Linux/Docker"
Frontend.allowsClientSideScripting = True
Frontend.codeType = "Managed"
Frontend.implementsAPI = False
Frontend.maxClassification = Classification.SENSITIVE
Frontend.minTLSVersion = TLSVersion.TLSv12
Frontend.tracksExecutionFlow = True
Frontend.usesEnvironmentVariables = True  # Model W uses env for config

Backend = Process("Django API")
Backend.inBoundary = application
Backend.levels = [2]
Backend.OS = "Linux/Docker"
Backend.allowsClientSideScripting = False
Backend.codeType = "Managed"
Backend.implementsAPI = True
Backend.maxClassification = Classification.SENSITIVE
Backend.minTLSVersion = TLSVersion.TLSv12
Backend.tracksExecutionFlow = True
Backend.usesEnvironmentVariables = True

CMS = Process("Wagtail Admin")
CMS.inBoundary = application
CMS.levels = [3]
CMS.codeType = "Managed"
CMS.implementsAPI = False
CMS.allowsClientSideScripting = True  # Wagtail admin uses significant JS
CMS.maxClassification = Classification.SENSITIVE

Worker = Process("Background Worker (Procrastinate)")
Worker.inBoundary = application
Worker.levels = [2]
Worker.OS = "Linux/Docker"
Worker.codeType = "Managed"
Worker.handlesResources = True  # Handles file system/storage tasks
Worker.usesEnvironmentVariables = True


# Datastores
Postgres = Datastore("PostgreSQL Database")
Postgres.inBoundary = internal_services
# Data Sensitivity Properties
Postgres.isShared = True  # Multiple processes (Backend, CMS, Worker) access
Postgres.storesPII = True  # Triggers privacy/GDPR related threats
Postgres.storesSensitiveData = True  # Marks this as a high-value target for attackers
# Architectural Properties
Postgres.hasWriteAccess = True  # Processes connecting to it can modify data
Postgres.isSQL = True  # Explicitly mark as SQL database
Postgres.onRDS = True  # Even if not AWS, it's a "Managed Service"
Postgres.type = DatastoreType.SQL  # Explicitly set type

Redis = Datastore("Redis Cache / Channels")
Redis.inBoundary = internal_services
# Data Sensitivity Properties
Redis.isShared = True  # Accessed by Backend, Workers, and Channels
Redis.storesPII = False  # Typically used for transient data, not PII
Redis.storesSensitiveData = True  # Because it holds session IDs and transient tokens
# Architectural Properties
Redis.hasWriteAccess = True  # All connected processes can write to it
Redis.isSQL = False  # Overriding the default since it's NoSQL/KV
Redis.onRDS = True  # Marks it as a managed cloud service
Redis.type = DatastoreType.UNKNOWN  # pytm lacks a 'CACHE' type;

Spaces = Datastore("DigitalOcean Spaces Object Storage")
Spaces.inBoundary = external_services
# Data Sensitivity Properties
Spaces.isShared = True  # Accessed by Backend and Workers
Spaces.storesPII = False  # Typically used for media assets, not PII
Spaces.storesSensitiveData = False  # While it can store files, we assume not sensitive
# Architectural Properties
Spaces.hasWriteAccess = True  # Backend/Workers can write
Spaces.isSQL = False
Spaces.onRDS = True  # Treated as a managed cloud service
Spaces.type = DatastoreType.AWS_S3  # Spaces uses the S3-compatible API

# External Entities
Sentry = ExternalEntity("Sentry Monitoring")
Sentry.inBoundary = external_services

Kerfu = ExternalEntity("Kerfu Foo Deployment Platform")
Kerfu.inBoundary = administration

GitHub = ExternalEntity("GitHub Source Control")
GitHub.inBoundary = supply_chain

CI = ExternalEntity("CI/CD Pipeline")
CI.inBoundary = supply_chain


# Data
user_session = Data(
    "User Session Cookie",
    description="Django session ID used for authentication",
    classification=Classification.SENSITIVE,
    isPII=False,
    isCredentials=True,
    credentialsLife=Lifetime.SHORT,
    isStored=True,
    isSourceEncryptedAtRest=True,
    isDestEncryptedAtRest=True,
)

user_credentials = Data(
    "User Credentials",
    description="Username and Password for login",
    classification=Classification.SENSITIVE,
    isPII=True,
    isCredentials=True,
    credentialsLife=Lifetime.MANUAL,
    isStored=True,
    isSourceEncryptedAtRest=False,  # Often cleartext in memory during login
    isDestEncryptedAtRest=True,
)

app_content = Data(
    "Application Content",
    description="General CMS content and public data",
    classification=Classification.PUBLIC,
    isPII=False,
    isCredentials=False,
    isStored=True,
)

pii_data = Data(
    "User Profile Data",
    description="Names, emails, and profile information",
    classification=Classification.SENSITIVE,
    isPII=True,
    isCredentials=False,
    isStored=True,
    isDestEncryptedAtRest=True,
)

db_connection_params = Data(
    "Database Secrets",
    description="Credentials used by Django to connect to Postgres/Redis",
    classification=Classification.RESTRICTED,
    isPII=False,
    isCredentials=True,
    credentialsLife=Lifetime.HARDCODED,
    isStored=True,
    isSourceEncryptedAtRest=True,
)

error_logs = Data(
    "Error Traces",
    description="Stack traces and debug info sent to Sentry",
    classification=Classification.SENSITIVE,
    isPII=True,
    isCredentials=False,
    isStored=True,
)

